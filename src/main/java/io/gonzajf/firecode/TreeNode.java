package io.gonzajf.firecode;

public class TreeNode {

	int data;
	TreeNode left;
	TreeNode right;

	TreeNode(int data, TreeNode left, TreeNode right) {
		this.data = data;
		this.left = left;
		this.right = right;
	}
	
	public static void main(String[] args) {
		System.out.println(12/1000);
		System.out.println(345/1000);
		System.out.println(2/1000);
		System.out.println(6/1000);
		System.out.println(7849/10);
		System.out.println(784/10);
		System.out.println(78/10);
		System.out.println(7/10);
	}

}
